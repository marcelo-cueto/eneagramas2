      <p><i>¿Cómo es el OCHO y qué le trae al DOS?</i> El OCHO es el “líder”. Siente
         visceralmente el anhelo de justicia. Capta instintivamente si algo no es genuino
         y desenmascara el doble juego a través de la agresión. Siempre es claro y
         directo a la hora de establecer límites y decir que no. El OCHO le trae al DOS
         la capacidad de cuidarse, de ponerse y poner límites. Lo lleva a realizarse las
         siguientes preguntas: <i>“¿Qué es lo que me mueve a ayudar al otro?”</i> Una vez
         que ha tomado lo que el OCHO le ofrece, inicia su camino hacia el CUATRO.
      </p>
      <p><i>¿Cómo es el CUATRO y qué le trae al DOS?</i> El CUATRO es “el creativo”.
         Siente intensamente la energía de la vida. Tiene una gran necesidad de intimidad
         que le brinda una profunda capacidad de autoconocimiento y honestidad
         interior. El CUATRO le trae a DOS la posibilidad de tomar distancia de los
         otros para poner el foco en sí mismo. Recién cuando ha podido poner límites
         (OCHO), conectarse consigo mismo y reconocer cuáles son sus necesidades
         (CUATRO) puede avanzar un paso y tomarse en cuenta (UNO).
