      <p><i>¿Cómo es el NUEVE y qué le trae al TRES?</i> El NUEVE es el “pacificador”.
         Siente visceralmente el anhelo de armonía. Tiene carácter ecuánime, tranquilo
         y equilibrado. El NUEVE le trae al TRES la posibilidad de pasar del hacer desenfrenado
         a parar. Lo lleva a realizarse las siguientes preguntas: <i>“¿Por qué busco
         el éxito? ¿Qué intento demostrar? ¿Por qué me da miedo detenerme?”</i>
         Una vez que ha tomado lo que el NUEVE le ofrece, inicia su camino hacia el
         SEIS.
      </p>
      <p><i>¿Cómo es el SEIS y qué le trae al TRES?</i> El SEIS es el “confiable”. Se caracteriza
         por la colaboración, la pertenencia y tradición, la responsabilidad y
         fidelidad. El SEIS posee una gran capacidad de trabajo en equipo, sabe llevar a
         término los proyectos y es capaz de sacrificarse a tal fin. El SEIS le trae al TRES
         la capacidad de pasar de formar equipos a integrar equipos. Recién cuando ha
         podido serenarse (NUEVE) y trabajar con otros (SEIS) puede avanzar un paso
         más en su camino y encontrar la paz interior (NUEVE).
