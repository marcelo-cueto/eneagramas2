      <h2>Brazos del CUATRO</h2>
      <p>Los brazos del CUATRO son: el <a href="/mirarme/eneatipo2">DOS</a> y el <a href="/mirarme/eneatipo1">UNO.</a> En un primer
         momento, el CUATRO se siente identificado con los aspectos del DOS y,
         posteriormente, con los del UNO.
      </p>
      <p><i>¿Cómo es el DOS y qué le trae al CUATRO?</i> El DOS es “el que da”. Sus
         dotes sociales y su capacidad de entrega lo llevan a intuir las necesidades del
         prójimo. El DOS le trae al CUATRO la posibilidad de pasar del vínculo interior al
         vínculo con el otro; esa capacidad de pensar en plural, de salirse de los nudos
         de auto-enredo y del egocentrismo. Lo lleva a realizarse la siguiente pregunta:
         <i>"¿Qué rol tienen los otros en mi vida?"</i> Una vez que ha tomado lo que el DOS
         le ofrece, inicia su camino hacia el UNO.
      </p>
      <p><i>¿Cómo es el UNO y qué le trae al CUATRO?</i> El UNO es “el idealista”. Se
         distingue por ser ordenado y auto-disciplinado. Su energía se canaliza en el
         esfuerzo por mejorar la realidad, tanto dentro como fuera de sí mismo (para
         contribuir a crear un orden moral). El UNO le trae al CUATRO la posibilidad de
         pasar de la dispersión a la disciplina, sin perder los ideales. Recién cuando ha
         podido ver a los demás (DOS) y ha aprendido a organizarse (UNO), puede avanzar
         un paso más en su camino y disfrutar la vida (SIETE).
      </p>
