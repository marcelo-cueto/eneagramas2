         alegría; a pasar de la estructura a lo lúdico. Lo lleva a realizase la siguiente pregunta:
         <i>“¿Vine a estudiar o vivir la vida?”</i> Una vez que ha tomado lo que el
         SIETE le ofrece, inicia su camino hacia el OCHO.
      </p>
      <p><i>¿Cómo es el OCHO y qué le trae al CINCO?</i> El OCHO es el “líder”. Lo que
         lo hace líder no es su función, sino su modo de ser y de relacionarse. Es capaz
         de luchar por las cosas en las que cree, sin dejarse manipular porque lo guían
         las convicciones interiorizadas, como el valor de la justicia que ocupa siempre
         el centro de su vida. El OCHO le trae a CINCO la capacidad de pasar de la reflexión
         a la gestión. Recién cuando ha aprendido a descontracturarse (SIETE) y
