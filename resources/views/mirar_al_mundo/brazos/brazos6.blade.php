      </p>
      <p><i>¿Cómo es el TRES y qué le trae al SEIS?</i> El TRES es “el exitoso”. En él
         pulsa el desafío de la vida. Le resulta fácil llevar a cabo sus cometidos de manera
         efectiva y competente, proponerse metas y cumplirlas. Se distingue por
         la claridad de sus objetivos, el alto nivel de energía que lo anima, por la confianza
         con que mira el futuro y la intensidad con que trabaja. Es una persona
         optimista, eficiente, práctica, emprendedora y confiada. El TRES le trae al SEIS
         la posibilidad de pasar de la indecisión a animarse, porque es accionando que
         el SEIS superará sus miedos. Lo lleva a realizarse la siguiente pregunta: <i>Si sé lo
         que tengo que hacer, ¿por qué no me atrevo?</i> Una vez que ha tomado lo que
         el TRES le ofrece, inicia su camino hacia el NUEVE.
      </p>
      <p><i>¿Cómo es el NUEVE y qué le trae al SEIS?</i> El NUEVE es el “pacificador”.
         Siente visceralmente el anhelo de armonía. Tiene un carácter ecuánime, tranquilo
         y equilibrado. Se caracteriza por una bondad y sencillez que lo hacen
         amable. Su modo calmo y natural de expresar “verdades duras”, hace que a los
         otros les resulte sencillo aceptarlas. El NUEVE le trae al SEIS la posibilidad de
         pasar del bloqueo (producto de sus temores) a la serenidad. Recién cuando ha
         podido animarse (TRES) con serenidad (NUEVE) puede avanzar un paso más en
         su camino y obrar con convicción (TRES).
      </p>
