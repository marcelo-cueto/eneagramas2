      <h2>Brazos del SIETE</h2>
      <p>Los brazos del SIETE son: el <a href="/mirarme/eneatipo1">UNO</a> y el <a href="/mirarme/eneatipo5">CINCO.</a> En un primer
         momento, el SIETE se siente identificado con los aspectos del UNO y, posteriormente,
         con los del CINCO.
      </p>
      <p><i>¿Cómo es el UNO y qué le trae al SIETE?</i> El UNO es “el idealista”. Su
         energía se canaliza en el esfuerzo por mejorar la realidad, tanto dentro como
         fuera de sí mismo (para contribuir a crear un orden moral). Tiene cualidades
         de líder estimulando a los demás a superarse a sí mismo. Se distingue por ser
         ético, confiable, productivo, ordenado, idealista y autodisciplinado. Valora la
         honradez y tiende a ser objetivo y equilibrado en sus juicios. El UNO le trae al
         SIETE la posibilidad de pasar de la desorganización al método. Lo lleva a realizarse
         la siguiente pregunta <i>¿Me estoy manejando de manera correcta para
         lograr mis objetivos?</i> Una vez que ha tomado lo que el UNO le ofrece, inicia
         su camino hacia el CINCO.
      </p>
      <p><i>¿Cómo es el CINCO y qué le trae al SIETE?</i> El CINCO es el “observador”.
         Observa la realidad para obtener de ella conocimiento y crecer. Es contemplativo,
         introvertido y solitario. Se distingue por la capacidad de análisis y síntesis:
         sabe catalogar conceptos, establecer conexiones entre ideas diferentes. Es
         objetivo, se cuestiona las causas e indaga la realidad en detalle. Posee óptimas
         capacidades críticas y de valoración. Su energía se expresa en la sistematicidad.
         El CINCO le trae al SIETE la capacidad de pasar del desorden a lo medular.
         Recién cuando ha podido disciplinarse (UNO) y ha logrado reflexionar objetivamente
         (CINCO), puede avanzar un paso más en su camino y accionar en forma
         sostenida (OCHO).
      </p>
