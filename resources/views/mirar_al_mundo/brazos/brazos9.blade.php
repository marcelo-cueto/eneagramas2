      <p>Los brazos del NUEVE son: el <a href="/mirarme/eneatipo6">SEIS</a> y el <a href="/mirarme/eneatipo3">TRES.</a> En un primer
         momento, el NUEVE se siente identificado con los aspectos del SEIS y posteriormente,
         con los del TRES.
      </p>
      <p><i>¿Cómo es el SEIS y qué le trae al NUEVE?</i> El SEIS es el “confiable”. No
         tolera situaciones ambiguas. Este eneatipo se caracteriza básicamente por la
         pertenencia y la fidelidad (familia, iglesia, equipo deportivo, partido político).
         Posee una gran capacidad de trabajo en equipo, sabe llevar a término los
         proyectos. Es una persona leal, responsable y dedicada. Sin embargo, también
         puede ser miedosa, indecisa, desconfiada. El SEIS le trae al NUEVE la posibilidad
         de pasar de la imparcialidad a la preferencia. Lo lleva a realizarse la siguiente
         pregunta <i>¿Por qué me cuesta tanto motivarme?</i> Una vez que ha tomado lo
         que el SEIS le ofrece, inicia su camino hacia el TRES.
      </p>
      <p><i>¿Cómo es el TRES y qué le trae al NUEVE?</i> El TRES es “el exitoso”. En él
         pulsa el desafío de la vida. Le resulta fácil llevar a cabo sus cometidos de manera
         efectiva y competente, proponerse objetivos y cumplirlos, así como apasionar
         a otras personas, motivarlas y capacitarlas. Se distingue por la claridad de
         objetivos, el alto nivel de energía que lo anima, por la confianza con la que mira
         el futuro y la intensidad con la que trabaja. Es una persona optimista, eficiente,
         emprendedora y enérgica. El TRES le trae al NUEVE la posibilidad de pasar de
         la pasividad a la acción. Recién cuando ha podido pasar de la imparcialidad a la
         preferencia (SEIS) y ha podido accionar (TRES), puede avanzar un paso más en
         su camino, comprometiéndose en cada proyecto (SEIS).
      </p>
